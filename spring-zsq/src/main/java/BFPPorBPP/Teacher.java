package BFPPorBPP;

public class Teacher {
    public String name;

    public Teacher() {
        System.out.println("Teacher.Teacher");
    }

    public Teacher(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Teacher{" +
                "name='" + name + '\'' +
                '}';
    }
}
